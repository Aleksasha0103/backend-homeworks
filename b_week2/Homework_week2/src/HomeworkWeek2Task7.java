public class HomeworkWeek2Task7 {
    public static void main(String[] args) {
        int b = 50;
        int c = 20;


        System.out.println("Исходные данные:");
        System.out.println("b = 50;");
        System.out.println("с = 20;");
        System.out.println("Увеличим b на 1 и уменьшим c на 1:");
        System.out.println(++b);
        System.out.println(--c);
        System.out.println("Проверим условие «b больше либо равно 51 или с меньше 22»:");
        System.out.println("Условие верно?");
        System.out.println(b >= 51 || c < 22);
    }

}
